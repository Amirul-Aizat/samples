package samples.demo;

public class Location {
	
	private int space;
	private int storage;
	
	public Location(int space, int storage) {
		this.space=space;
		this.storage=storage;
	}


	public int getSpace() {
		return space;
	}


	public void setSpace(int space) {
		this.space = space;
	}


	public int getStorage() {
		return storage;
	}


	public void setStorage(int storage) {
		this.storage = storage;
	}
	
	
}
